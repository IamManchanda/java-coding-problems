/**
 * Problem: Check if a number is even or not.
 */

public class Solution {
    public boolean checkEven(int n) {
        if (n % 2 == 0) {
            return true;
        }

        return false;
    }
}

/*
 * Enter the integer:
 * 10
 * 
 * Result:
 * 10 is even.
 */

/*
 * Enter the integer:
 * 11
 * 
 * Result:
 * 11 is not even.
 */
