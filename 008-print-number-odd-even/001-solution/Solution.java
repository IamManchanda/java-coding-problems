/**
 * Problem: Print if number is odd or even.
 */

public class Solution {
    public String printNumberOddEven(int number) {
        if (number % 2 == 0) {
            return "EVEN";
        }

        return "ODD";
    }
}

/*
 * Enter the number:
 * 3
 * 
 * The number 3 is:
 * ODD
 */

/*
 * Enter the number:
 * 4
 * 
 * The number 4 is:
 * EVEN
 */
