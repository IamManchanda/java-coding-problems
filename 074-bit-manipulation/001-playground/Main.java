public class Main {
    public static void main(String[] args) {
        int binAnd = 5 & 6;
        System.out.println("5 & 6 = " + binAnd); // 4

        int binOr = 5 | 6;
        System.out.println("5 | 6 = " + binOr); // 7

        int binXor = 5 ^ 6;
        System.out.println("5 ^ 6 = " + binXor); // 3
    }
}
